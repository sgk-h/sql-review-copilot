#!/usr/bin/env bash
# =============================================================
# cron_setup.sh — crontab 登録エントリのサンプルを出力する
# 実行すると crontab に追加するための設定例を表示する
# =============================================================

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SCRIPT_DIR}/config.sh"

RUN_SCRIPT="${SCRIPT_DIR}/run_sql_review.sh"
CRON_LOG="${LOGS_DIR}/cron.log"

cat <<EOF
================================================================
  SQL自動レビュー — crontab 登録手順
================================================================

【手順1】トークンを ~/.copilot_env に保存する（推奨）

  $ cat >> ~/.copilot_env <<'ENVEOF'
  export COPILOT_GITHUB_TOKEN=github_pat_ここにトークンを貼り付け
  ENVEOF
  $ chmod 600 ~/.copilot_env

  ※ ~/.bash_profile や ~/.zprofile への直接記載でも可。
  ※ セキュリティのため crontab にトークンを直書きするのは非推奨。

【手順2】crontab を編集する

  $ crontab -e

【手順3】以下のエントリを追加する

  # SQL自動レビュー — 毎日午前2時に実行
  0 2 * * * source ~/.copilot_env && bash ${RUN_SCRIPT} >> ${CRON_LOG} 2>&1

  ┌─ 分 (0-59)
  │ ┌─ 時 (0-23)  ← 2 = 午前2時
  │ │ ┌─ 日 (1-31)  * = 毎日
  │ │ │ ┌─ 月 (1-12)  * = 毎月
  │ │ │ │ ┌─ 曜日 (0-7)  * = 毎曜日
  0 2 * * *

【手順4】登録を確認する

  $ crontab -l

================================================================
  オプション: 実行頻度を変える場合の参考
================================================================

  毎時0分に実行:    0 * * * *
  平日のみ午前2時:  0 2 * * 1-5
  週1回（月曜2時）: 0 2 * * 1

================================================================
  手動実行コマンド（動作確認用）
================================================================

  # 差分のみ（通常実行）
  source ~/.copilot_env && bash ${RUN_SCRIPT}

  # 全ファイル強制再チェック
  source ~/.copilot_env && bash ${RUN_SCRIPT} --force

  # APIを呼ばずに対象ファイルだけ確認
  source ~/.copilot_env && bash ${RUN_SCRIPT} --dry-run

================================================================
EOF
